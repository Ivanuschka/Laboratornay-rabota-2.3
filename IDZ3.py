word = input("Введите слово: ")
k = int(input("Введите номер буквы для удаления (считая с 1): "))

# Удаление третьей буквы
if len(word) >= 3:
    word = word[:2] + word[3:]

# Удаление k-й буквы (где k - индекс буквы, считая с 1)
if 1 <= k <= len(word):
    word = word[:k-1] + word[k:]

print("Слово после удаления третьей буквы и", k, "-й буквы:", word)
